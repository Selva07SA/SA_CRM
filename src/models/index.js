const { Sequelize } = require('sequelize');
const config = require('../config/database.js');

const env = process.env.NODE_ENV || 'development';
const dbConfig = config[env];

const sequelize = new Sequelize(
  dbConfig.database,
  dbConfig.username,
  dbConfig.password,
  {
    host: dbConfig.host,
    port: dbConfig.port,
    dialect: dbConfig.dialect,
    logging: dbConfig.logging,
    pool: {
      max: 5,
      min: 0,
      acquire: 30000,
      idle: 10000
    }
  }
);

const db = {};

// Import models
db.Employee = require('./Employee')(sequelize, Sequelize.DataTypes);
db.Client = require('./Client')(sequelize, Sequelize.DataTypes);
db.Lead = require('./Lead')(sequelize, Sequelize.DataTypes);
db.SubscriptionPlan = require('./SubscriptionPlan')(sequelize, Sequelize.DataTypes);
db.Subscription = require('./Subscription')(sequelize, Sequelize.DataTypes);

// Define associations
Object.keys(db).forEach(modelName => {
  if (db[modelName].associate) {
    db[modelName].associate(db);
  }
});

db.sequelize = sequelize;
db.Sequelize = Sequelize;

module.exports = db;
